void Spin(float* x, float* y, Angle angle) {
	Spin(x, y, angle.sin, angle.cos);
}
void Spin(float* x, float* y, float sin, float cos) {
	float temp = *x * cos - *y * sin;
	*y = *x * sin + *y * cos;
	*x = temp;
}
